num = "+34-91478248-76"
#     123456789...
print(num[2])  # impime el 4
print(num[-2])  # Imprime el 7

# slice
print(num[1:3])  # imprime 34 por que comienza en el 1 a contar hasta el 3
print(num[5:])  # imprime 1478248-76 por q comineza desde 5 al final
print(num[1:7:2])  # cuenta desde la posicion 1 hasta el 7 y escoge cada 2 numeros
print(num[::-1])  # da el resultado al reves
print(num[:5])  # cuenta desde 0 hasta 4
